
                <div class="section-name"> Nechránení hráči </div>
                    <br>
                    Hráči vo veku 27 rokov a viac, ktorým ostáva do ukončenia zmluvy 8 dni, sa automaticky dostanú na trh nechránených hráčov – manažérovi príde upozornenie v novinkách mužstva - v čase od 6:00 do 21:59 HA času. 
                    <a class="novinky" href="https://www.hockeyarena.net/sk/index.php?p=public_ha_news.php&id=13434"><img class="icons" src="./icons/novinka.png"></a> Počas prvých dní na trhu posielajú hráčovi ponuky cudzie mužstvá, 
                    podobne ako je to u predávaných hráčov. Hráčove domáce mužstvo má potom možnosť dorovnať najvyššiu ponuku z ostatných mužstiev počas nasledujúcich dní až do konca jeho aktuálnej zmluvy. Hráči podpisujú zmluvu na 210
                    dní. Materské mužstvo môže hráčovi ponúknuť zmluvu na 1, 2, alebo 3 sezóny (podľa <a href="index.php?navod=dalsie_atributy">lojality</a>).
                    <br><br>
                    <u> Hlavné odlišnosti nechráneného hráča : </u>
                    <br>
                    <ul>
                        <li> je možné predať ho, iba ak je v mužstve aspoň 10 týždňov (70 dni) a iba ak do skončenia jeho aktuálnej zmluvy ostáva viac ako 14 dní . </li>
                        <li> podrobné informácie o konkrétnych parametroch nechráneného hráča sa nachádzajú v detaile hráča pod linkou 'Zmluva'. Tu sa nachádza aj výška odstupného, ktoré musí zaplatiť nové mužstvo pôvodnému mužstvu, ak sa hráč rozhodne nezostať vo svojom aktuálnom
                            mužstve. Výšku odstupného určuje systém automaticky na základe vlastností hráča. Je tu aj informácia o hráčovej lojalite a aké verzie novej zmluvy bude umožňovať, ako aj o spokojnosti hráča v mužstve. </li>
                        <li> ďaľšia možnosť je rýchlo uvolniť nechráneného hráča pre jeho odchod do nového mužstva, v prípade že aktuálny vlastník sa rozhodol nepredĺžiť hráčovi zmluvu <a class="novinky" href="https://www.hockeyarena.net/sk/index.php?p=public_ha_news.php&id=13748"><img class="icons" src="./icons/novinka.png"></a> a v prípade, že hráčova zmluva je aspoň 3 dni alebo viac. 
                            <a class="novinky" href="https://www.hockeyarena.net/sk/index.php?p=public_ha_news.php&id=13776"><img class="icons" src="./icons/novinka.png"></a> Pri zvolení tejto možnosti sa skráti okamžite aktuálna hráčova zmluva na 0 dní a vlastník pri odchode hráča získa o 10% vyššiu kompenzáciu, tj. 80% miesto 70%. 
                            Hráč odíde do nového mužstva do pár minút, nie pri najbližšom prepočte.  </li>
                        </ul>
