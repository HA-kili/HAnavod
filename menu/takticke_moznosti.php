                    <h1><b>Hurá systém:</b></h1>
                    Formácia hrá bez taktickej špecializácie, v podstate chaoticky a len podľa intuície. <b> Pri hurá systéme sa hráči vo formácii nezohrávajú! </b>
                    <br><br>
                    <h1><b>Protiútoky:</b></h1>
                    Hráči sa budú sústrediť na dôkladnú obranu a budú čakať na chybu súpera po ktorej sa okamžite budú pokúšať o rýchly protiútok.
                    <br><br> 
                    <h1><b>Napádanie:</b></h1>
                    Hráči sa budú súpera snažiť obrať o puk dôsledným napádaním v jeho obrannom pásme a v strednom pásme.
                    <br><br> 
                    <h1><b>Krátke nahrávky:</b></h1>
                    Prechod do útočného pásma sa bude odohrávať pomocou rýchlych krátkych nahrávok.
                    <br><br> 
                    <h1><b>Obrana (checking):</b></h1>
                    Útočníci aj obrancovia sa budú sústrediť iba na bránenie.
                    <br><br> 
                    <h1><b>Streľba od modrej:</b></h1>
                    Obrancovia budú strieľať na bránu súpera od modrej čiary.
                    <br><br> 
                    <h1><b>Nahrávky spoza brány:</b></h1>
                    Center sa bude snažiť rozdávať gólové prihrávky spoza brány.
                    <br><br><br>
                    Každá z taktických špecializácií bude mať iné požiadavky na jednotlivé pozície hráčov a od toho bude závisieť jej úspešnosť. Konkrétne požiadavky na hráčske pozície pre tieto taktiky nájdete 
                    v menu <a class="arena" href="https://www.hockeyarena.net/sk/index.php?p=manager_lines_form_new.php"> Hráči - Zostava - Formácie</a>. 
                    <br><br>
                    <u> Maximálna možná zohranosť formácie </u> – najlepšie tri možné taktiky pre konkrétnu formáciu už nebudú tak výrazne zvýhodnené s cieľom umožniť lepšiu použiteľnosť všetkých taktík a protitaktík. 
                    <br>
                    Zrušenie miernej výhody taktiky "Hurá systém" proti iným taktikám – táto zmena nadobudne účinnosť od 29.5.2021. 
