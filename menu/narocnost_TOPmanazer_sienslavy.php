          <b>Náročnosť ligy</b>
          <br>
          Náročnosť ligy sa počíta ako rozdiel úrovne hráčov všetkých mužstiev v lige od priemernej ligovej úrovne (čím menší rozdiel, tým vyššia náročnosť) a podľa počtu aktívnych manažérov. 
          Nájdete ju v Menu <a class="arena" href="https://www.hockeyarena.net/sk/index.php?p=public_league_info.php">Liga - Tabuľka - Info</a> . 
          <br><br><br>
          <b>TOP manažér</b>
          <br>
          Body v hodnotení manažérov sa udeľujú podľa počtu víťazstiev, remíz a náročnosti ligy. Hodnotenie nájdete v Menu <a class="arena" href="https://www.hockeyarena.net/sk/index.php?p=public_managers_statistics.php">Manažér - TOP</a>. 
          Prvý manažér bude ocenený tým, že dostane <a class="navod" href="index.php?navod=sponzorsky_balik#TOPmanazer">trofej</a> do svojej zbierky. 
