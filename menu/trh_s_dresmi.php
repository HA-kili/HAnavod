                    <b><u> Trh s dresmi </u></b> 
                    <a class="novinka" href="https://www.hockeyarena.net/sk/index.php?p=public_ha_news.php&id=14358"><img class="icons" src="./icons/novinka.png"></a>
                    <br><br>
                    Dresy sa odteraz budú dať predať iba separátne na trhu s dresmi, čiže už sa nebude dať predať dres spolu "s hráčom". Dres predaný s hráčom bude automaticky označený ako nedarovateľný a nepredajný.
                    <br><br>
                    Predať dres je možné cez link "Poslať na trh" z obrazovky konkrétneho dresu. 
                    <span class="trh_s_dresmi">  Po kliknutí bude dres odoslaný na trh, po úspešnom predaji tím obdrží systémom určenú predajnú cenu, bez ohľadu na to, koľko zaplatí kupujúci! </span>
                    <br><br>
                    Trh s dresmi nájdete v <a class="arena" href="https://www.hockeyarena.net/sk/index.php?p=team_store.inc&action=list">Menu - Hráči - Dres</a>. 
                    <br><br>
                    <b>!!! Pozor !!!</b>
                    <br>
                    Odteraz pri umiestnení hráča na trh, všetky jeho dresy budú automaticky označené ako použité, takže ich už nebude možné darovať ani predať!
                    <br>
                    Prakticky to znamená to, že predať dres sa bude dať iba priamo cez trh s dresmi a nie cez trh s hráčmi. 
                    <br><br><br>
                    Trh s dresmi bol schválne vytvorený tak, aby bolo čo najťažšie špekulovanie a podvádzanie, tj. presúvanie si financií medzi 2 mužstvami cez trh s dresmi, z toho dôvodu aj nie je možné jednoducho dohladať 
                    svoj predávaný dres v zozname na trhu, pri umiestnení dresu na trh sa uzávierka znáhodňuje a predovšetkým predajná cena je určená systémom, len čiastočne ovplyvnená nákupnou cenou a to len dohodobo. 
                    To isté sa bude týkať aj ďalších typov tovaru ktoré chystáme pre tento trh, ako sponzori, darčekové predmety, skautovia, dunčovia. Ďakujeme za pochopenie.
                    <br>
                    Na trhu s dresmi sme doplnili link "podmienky darovania" pri každom konkrétnom drese sa ukážu podmienky darovania toho konkrétneho dresu. 
