
                <div class="section-name"> Motivačná odmena </div>
                    <br>
                    Motivačná odmena sa zadáva ako suma pre jedného hráča za jeden zápas. Vypláca sa iba v prípade výhry každému hráčovi v zostave (vrátane náhradného brankára). Zvyšuje individuálnu motiváciu hráčov relatívne k dennému platu
                    hráča. Odporúča sa používať najmä pri dôležitých zápasoch. Nastavuje sa v menu <a href="https://www.hockeyarena.net/sk/index.php?p=manager_tactics_form.php"> Mužstvo - Nastavenie zápasu</a>. 
                    <br><br> 
                    Ak mužstvo nepoužije na ligovom playoff zápase svoju najlepšiu zostavu (minimálne prvé dve najlepšie formácie), súperovi hráči sa <a href="https://www.hockeyarena.net/sk/index.php?p=public_ha_news.php&id=13061"> vzdajú odmien</a> za víťazstvo, 
                    ale ich motivácia na zápase ostane ako keby dostali odmenu. O tomto bude súper informovaný v novinkách mužstva. 
                    <br><br>
                    Maximálna účinná odmena je 10 násobok denného platu hráča! 
                    <br><br>
                    <b>Maximálna výška</b>  odmeny na zápas je <b>3 000 000</b> .
