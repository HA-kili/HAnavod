
                <div class="section-name"> Zástup </div>
                    <br>
                    V menu <a class="arena" href="https://www.hockeyarena.net/sk/index.php?p=manager_settings_form.php">Manažér - Nastavenie</a>  je možnosť 
                    nastaviť iného manažéra, ktorý pri Vašej absencii bude dohliadať na chod Vášho mužstva. Zástupca manažéra sa môže prihlásiť do 
                    Vášho mužstva použitím Vášho nick-u a jeho hesla. <a class="novinka" href="https://www.hockeyarena.net/sk/index.php?p=public_ha_news.php&id=7659"><img class="icons" src="./icons/novinka.png"></a>
                    <br><br>
                    Zástupca nebude mať samozrejme k dispozícii všetky funkcie, ale iba tie najdôležitejšie pre chod mužstva. 
                    <br><br>
                    <u><h1> Nebude mať k dispozícii tieto možnosti: </h1></u>  
                    predaj hráčov
                    <br> posielať ponuky na hráčov 
                    <br> vyhľadávať hráčov na trhu
                    <br> vyhodiť hráča
                    <br> demolovať štadión
                    <br> zmazať alebo zmeniť tréningový režim (ale bude môcť priradovať existujúce režimy hráčom)
                    <br> zmeniť nastavenia manažéra
                    <br> zmeniť sponzorské nastavenia
                    <br> podrobne skúmať hráčov a zápasy 
                    <br> používať v jeho mene diskusné fóra
                    <br> používať poštu 
                    <br><br><br>
                    Na druhej strane, zástupca môže používať funkcie reprezentačného manažéra (okrem pridávania a vyhadzovania hráčov). 
