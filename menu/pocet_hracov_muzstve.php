                    <b><h1> Počet hráčov v mužstve </h1></b>
                    <br><br>
                    Ak je v mužstve <u> menej ako 20 hráčov </u> , manažér mužstva bude prepustený <a href="https://www.hockeyarena.net/sk/index.php?p=public_rules_new.php"> (pozri Pravidlá hry)</a> a repre hráči tohto mužstva pôjdu na NT. 
                    To isté platí, ak sa manažér do mužstva neprihlási 42 dní.  
                    <br><br>
                    Ak je v mužstve <u> viac ako 64 hráčov </u> , do mužstva nechodia juniori a nie je možné kupovať a zamestnávať nových hráčov. 
