                        <li> pridelenie mužstiev novým manažérom </li>
                        <li> zápasy prebiehajú (19:00, 20:00, 21:00, 22:00 – <a href="index.php?navod=krajiny">podľa krajín</a> ) </li>
                        <li> tréning a regenerácia hráčov </li>
                        <li> dôkladný prieskum hráčov (po 22:00 ku koncu prepočtu) </li>
                        <li> vyhodnotenie hviezd týždňa </li>
                        <li> a rôzne iné </li> 
                    </ul>
                    
