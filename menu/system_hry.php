                    <br><br> 
                    Hockey Arena (HA) je hokejová hra, kde sa staráš o hokejové mužstvo, s cieľom dosiahnuť najlepšie výsledky v konkurencii ďalších manažérov z celého sveta.
                    <br><br> 
                    V mužstve plníš úlohu manažéra a trénera <b>(nie majiteľa !!)</b>. Kupuješ a predávaš hráčov, zlepšuješ zázemie, vyberáš mladé talenty z juniorky, nastavuješ zostavu a taktiku na zápasy, priraďuješ hráčom tréningový režim a mnoho iných vecí.
                    <br><br> 
                    Kompletnú starostlivosť o mužstvo vykonávaš na stránkach hry - sleduješ posledné udalosti v mužstve <a href="https://www.hockeyarena.net/sk/index.php?p=manager_news.php">(Novinky)</a> , nastavuješ zostavu na nasledujúci 
                    zápas <a href="https://www.hockeyarena.net/sk/index.php?p=manager_lines_lineup_form_new.php">(Zostava)</a> , získavaš hráčov na trhu <a href="https://www.hockeyarena.net/sk/index.php?p=manager_player_market_form.php">(Trh)</a>  a mnoho ďaľších vecí....
                    <br><br>
                    Každý deň prebieha takzvaný "<u>prepočet hry</u>", ktorý sa spúšťa automaticky o 19:00 hod a končí cca po 22:20 hod. Začiatok prepočtu trvá asi 1-2 minúty a hra je v tom čase uzavretá. Potom prepočet prebieha na pozadí a ty môžeš pokračovať
                    v riadení svojho hokejového mužstva (mužstiev: A, Au23, B, Bu23) a nastavovaní zmien.
                    <br><br>
                    <u><h1> V prepočte hry sa postupne spracuje nasledovné : </h1></u> 
