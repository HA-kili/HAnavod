
        <div class="section-name"> Náročnosť </div>
          <br>
          Náročnosť ligy sa počíta ako rozdiel úrovne hráčov všetkých mužstiev v lige od priemernej ligovej úrovne (čím menší rozdiel, tým vyššia náročnosť) a podľa počtu aktívnych manažérov. 
          Nájdete ju v Menu <a href="https://www.hockeyarena.net/sk/index.php?p=public_league_info.php">Liga - Tabuľka - Info</a> . 
          <br><br> 
          Body v hodnotení manažérov sa udeľujú podľa počtu víťazstiev a remíz a náročnosti ligy. Hodnotenie nájdete v Menu <a href="https://www.hockeyarena.net/sk/index.php?p=public_managers_statistics.php">Manažér - TOP</a> .
