
                <div class="section-name"> Registrácia </div>
                    <br>
                    <b><h2> Prvý krok je vytvorenie nového konta v hre : </h2></b>
                    <br>
                    <i><u><h1> Nick </h1></u></i> - meno alebo prezývka, s ktorou bude manažér vystupovať v hre. Nickname je možné zmeniť 1* za celú HA kariéru. 
                    <br><br>
                    <i><u><h1> Heslo </h1></u></i> - každý manažér si vytvorí heslo, ktorým sa bude prihlasovať do hry.  
                    <br><br>
                    <i><u><h1> E-mail </h1></u></i> - manažér si nastaví aktívnu e-mailovú adresu. Po pridelení mužstva príde manažérovi na túto adresu automaticky upozornenie. 
                    <br><br>
                    <i><u><h1> Štát </h1></u></i> - manažér si vyberie štát, v ktorom chce hrať. 
                    <br><br><br><br>
                    <b class="red"> Upozornenie - je zakázané registrovať sa a hrať v inej ako vlastnej reálnej krajine (v prípade že tá krajina existuje) !!! </b>
