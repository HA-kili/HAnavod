
                <div class="section-name"> Pokročilé zápasové taktiky </div>
                    <br><br>
                    Pripravili sme úplne nový systém nastavenia taktiky na zápas, kde je možné ovplyvniť viaceré taktické nastavenia aj počas zápasu, podľa aktuálneho skóre alebo iných udalostí. Všetky potrebné informácie
                    nájdete v menu <a href="https://www.hockeyarena.net/sk/index.php?p=manager_tactics_adv.inc&tactics_id=65824&return_url=manager_tactics_form.php&tactics_id=65824&desc=%3Cb%3ETu+si+m%C3%B4%C5%BEe%C5%A1+nastavi%C5%A5+v%C5%A1eobecn%C3%A9+parametre+z%C3%A1pasu.+M%C3%B4%C5%BEe%C5%A1+ma%C5%A5+ulo%C5%BEen%C3%BDch+celkovo+5+r%C3%B4znych+nastaven%C3%AD.%3C/b%3E">Mužstvo - Nastavenie zápasu - Nastavenie taktiky</a> .
                    Taktiku je možné nastaviť pre konkrétny zápas, alebo si prednastaviť zápasové profily, pre ligové, priateľské alebo iné zápasy, ktoré sa potom automaticky použijú pre všetky zápasy toho typu.
                    <br><br>
                    Taktické akcie menia taktické nastavenia v priebehu zápasu podľa jednej alebo dvoch zvolených podmienok.
                    <br><br><br>
                    <b><h1> Stiahnuť / pridať (vrátiť)  ľubovoľnú formáciu počas zápasu </b></h1> 
                    Tréner má možnosť počas zápasu stiahnuť alebo pridať ľubovoľnú formáciu z hry za určitých podmienok, a tak zlepšiť výkon svojho mužstva. Stiahnutú formáciu je možné vrátiť
                    späť do zápasu.
                    <br><br><br>
                    <b><h1> Striedanie brankára </b></h1> 
                    Tréner má tiež možnosť vystriedať brankára za náhradného podľa zvolených podmienok.
                    <br><br><br>
                    <b><h1> Zmeniť čas na ľade </b></h1>
                    Zmení čas, po ktorom sa na ľade striedajú formácie. Pôvodne nastavený čas je možné zistiť v obrazovke zápasu v štatistike formácií.
                    <br><br><br>
                    <b><h1> Kopírovať súperove formácie </b></h1>
                    Toto nastavenie spôsobí, že formácie budú na ľad nastupovať presne podľa súperových formácií, čiže prvá formácia nastúpi so súperovou prvou, druhá s druhou atď. V prípade, že túto možnosť zapnú obidvaja súperi,
                    prispôsobuje sa hosťujúce mužstvo domácemu. Ak nastavíte na zápas 4 formácie, súper iba 3 a zapnete kopírovanie formácií, tak sa na zápase budú striedať len vaše prvé 3 formácie. Ak nastavíte na zápas len 3 formácie a súper nastaví
                    4, nebude možné použiť kopírovanie formácií.
                    <br><br><br>
                    <b><h1> Rozdelenie taktických bodov </b></h1> 
                    Manažér má k dispozícii celkom 30 bodov na rozdelenie medzi jednotlivé tretiny a herné činnosti. Rozdelené body zvyšujú výkonnosť mužstva v danej hernej činnosti – útoku alebo obrane. Čím viac
                    bodov, tým vyšší výkon. Na predĺženie je možné rozdeliť extra 10 taktických bodov.
                    <br>
                    <a class="red"> Napríklad 'Taktické body ofenzíva 15 defenzíva 5' nastaví bonus 15:5 v prospech ofenzívy. Môžete použiť všetky body v jednej tretine alebo ich môžete rozdeliť rovnomernejšie na celý zápas.
                    Body nastavené v prvej tretine sa použijú na celý zápas, ak nebude toto rozdelenie zmenené v ďalších tretinách.
                    Ak mužstvo prekročí počas zápasu limit 30 bodov na jeden zápas, systém použije maximum zostávajúcich bodov v aktuálnej tretine a zvyšok zápasu sa odohrá s 0:0.
                    <br>
                    Napríklad nastavenie 20:0 znamená:
                    <br>
                    1. period - 20:0
                    <br>
                    2. tretina - 10:0
                    <br>
                    3. tretina - 0:0 </a>
                    <br><br><br>              
                    <b><h1> Agresivita </b></h1> 
                    Manažér má možnosť počas zápasu meniť agresivitu podľa zvolených podmienok. Agresivita v spojení so silou a sebaovládaním hráča môže mať pozitívny vplyv na celkovú prevahu formácie na ľade, ale pri nízkom sebaovládaní
                    hráčov spôsobuje častejšie vylučovanie. Úroveň agresivity ovplyvňuje aj rýchlosť ubúdania energie hráčov :
                    <br>
                    <br> Nízka - spotreba energie: 100% 
                    <br> Stredná - spotreba energie: 102% 
                    <br> Vysoká - spotreba energie: 105% 
                    <br> Rež a rúbaj - spotreba energie: 111%

                    <br><br><br>
                    <b><h1> Mierenie streľby </b></h1>
                    Manažér môže nastaviť dve zmeny mierenia streľby na bránu počas zápasu. 
                    <br><br><br>
                    <b> PODMIENKY
                    <br> 
                    Podmienky sa vyhodnocujú počas zápasu a určujú, či sa akcia vykoná, alebo nie. Keď sú všetky podmienky priradené k akcii splnené, akcia sa vykoná a je deaktivovaná do konca zápasu. </b>
                    <br><br><br>
                    <b> None </b>
                    <br> 
                    Akcia sa vykoná po dosiahnutí zvoleného času zápasu, bez akýchkoľvek podmienok.
                    <br>
                    <b> Prehrávame aspoň o ? góly (0 = remíza) </b>
                    <br> 
                    Vyhodnocuje sa raz pri dosiahnutí času akcie a potom vždy, keď dostaneme gól. Môžu byť použité aj záporné hodnoty. Napríklad -1, dostaneme gól a vyhrávame stále o 1 gól, táto podmienka sa vyhodnotí ako splnená.
                    <br>
                    <b> Vyhrávame aspoň o ? góly (0 = remíza) </b>
                    <br> 
                    Vyhodnocuje sa raz pri dosiahnutí času akcie, a potom vždy, keď skórujeme. Môžu byť použité aj záporné hodnoty. Napríklad –1, ak skórujeme a prehrávame stále o 1 gól, táto podmienka sa vyhodnotí ako splnená.
                    <br>
                    <b> Brankár dostal ? gólov počas ? minút </b> 
                    <br> 
                    Vyhodnocuje sa vždy, keď dostaneme gól, ak sme dostali zadaný počet gólov v intervale rovnakom alebo menšom ako je zadaný čas, podmienka je splnená.
                    <br> Príklad:
                    <br> Góly sme dostali v čase 0:00 a 10:00
                    <br> Brankár dostal 2 góly počas 11 minút – splnená podmienka
                    <br> Brankár dostal 2 gólov počas 9 minút – nesplnená podmienka
                    <br>
                    <b> Náš brankár stratil koncentráciu </b> 
                    <br> 
                    Brankár môže stratiť koncentráciu a sebadôveru, keď dostane viac gólov v krátkom čase, ak sa to stane, bude táto podmienka splnená. Brankár ktorý stratil koncentráciu, chytá ďalej oveľa horšie, ako sú jeho schopnosti.
                    <br>
                    <b> Súper stiahol ľubovoľnú formáciu </b>
                    <br> 
                    Vyhodnocuje sa raz pri dosiahnutí času akcie, ak už súper stiahol zo zápasu formáciu, podmienka bude splnená. Kontroluje sa znovu, keď súper stiahne ľubovoľnú formáciu neskôr.
