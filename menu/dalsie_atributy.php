                    <b> Lojalita</b> 
                    <br> 
                    Vlastnosť hráča, ktorá ovplyvňuje možnosti podpísania novej zmluvy v aktuálnom mužstve nechráneného hráča. 
                    <br><br>
                    <u> Verzie zmluvy : </u>
                    <br> 1. zmluva na 70 dní , podpisový bonus 0, plat hráča sa vypočíta tak, že k tabuľkovému platu hráča sa pripočíta 1/70 časť z 45% aktuálnej najvyššej ponuky z iných mužstiev 
                    <br> 2. zmluva na 70 dní , podpisový bonus 40% z aktuálnej najvyššej ponuky z iných mužstiev, tabuľkový plat hráča 
                    <br> 3. zmluva na 140 dní , podpisový bonus 70% z aktuálnej najvyššej ponuky z iných mužstiev, tabuľkový plat hráča 
                    <br> 4. zmluva na 210 dní , podpisový bonus 100% z aktuálnej najvyššej ponuky z iných mužstiev, tabuľkový plat hráča 
                    <br><br><u> Lojalita hráča môže mať 6 úrovní </u> - možnosti zmluvy pre majiteľa hráča podľa jednotlivých úrovní budú takéto:
                    <br>
                    <br> -3 : šanca udržať si hráča na 210 dní bude zvíťaziť nad ostatnými mužstvami pri navyšovaní jeho ponuky na trhu (možnosť automatickej ponuky) alebo hráčovi s lojalitou -3 je možné najvyššiu ponuku ešte nakoniec preponúknuť jeho majiteľom za 1,2-násobok, takto vzniknutá zmluva bude na 70 dní. 
                    <br> -2 : hráč bude akceptovať verzie zmluvy 4
                    <br> -1 : hráč bude akceptovať verzie zmluvy 3, 4
                    <br> 1 : hráč bude akceptovať verzie zmluvy 2, 3, 4
                    <br> 2 : hráč bude akceptovať verzie zmluvy 1, 2, 3, 4
                    <br> 3 : hráč zostane v mužstve dovtedy, kým sa manažér nerozhodne predať alebo vyhodiť ho 
                    <br><br>
                    <u> Vernostný bonus </u>
                    <br> 
                    Manažér bude môcť každú sezónu vybrať 1 hráča zo základnej zostavy, ktorému vyplatí <u><i> tzv. vernostný bonus </i></u>. Tento bonus zvýši natrvalo hráčovu lojalitu, ale na druhej strane môže mať negatívny vplyv na lojalitu iných hráčov,
                    ktorí bonus nedostali. Vyplatiť bonus môžete cez linku "Zmluva" z detailu hráča alebo cez <img style="icons" src="./icons/plusko.gif"> , ktoré nájdete tiež v detailnom okne hráča. O pozitívnom aj negatívnom vplyve budete detailne informovaní pred vyplatením konkrétneho bonusu. Lojalita sa nedá meniť u hráčov, ktorí sú aktuálne na trhu
                    a týka sa to pozitívnej aj negatívnej zmeny. Na zmenu lojality hráča je potrebné splniť podmienku 20 odohraných zápasov za sezónu (rátajú sa zápasy základnej časti a playoff, playout nie). <span class="red">Výšku vernostného bonusu ovplyvňuje ekonomická úroveň mužstva a aktuálna lojalita hráča.</span>
