                    <br>
                    <b> Vek</b> 
                    <br> Vek hráča sa zvyšuje o jeden rok na konci každej sezóny. Keď hráč dosiahne svoj vekový horizont, jeho hlavné atribúty začnú pomaly klesať.
                    Jediný atribút, čo neklesá (až od veku 41 rokov) je sebaovládanie. 
                    <br><br>
                    <b> Zmluva</b> 
                    <br> 
                    Počet zostávajúcich dní hráčovej zmluvy. 
                    <br> 
                    V detailnom zobrazení vlastného hráča sa nachádza možnosť "Vyhodiť hráča". Vyhodení hráči
                    s kvalitou nižšou ako 80 odchádzajú automaticky do dôchodku , ostatní sa snažia 'zamestnať sa' v iných mužstvách. Zmluvy všetkých hráčov budú automaticky predĺžené v prípade, že má mužstvo dostatok hotovosti
                    na podpisový bonus.
                    <br><br>
                    <b> Zranenie </b>
                    <br> Typ zranenia a koľko dní bude ešte hráč zranený.
                    <br><br>
                    <b> Plat</b>
                    <br> Denný plat hráča podľa jeho aktuálnej zmluvy.
                    Plat pre chránených hráčov sa počíta podľa ich schopností, hviezdneho štatútu a podľa úrovne ligy. Zahraničný hráč v mužstve dostáva k svojmu zmluvne dohodnutému platu navyše bonus 15%.
                    Plat hráčov, ktorí hrajú v sobotu repre zápas (základná zostava) a chýbajú svojmu materskému klubu, platí reprezentácia. 
                    <br><br>
                    <b> Štát</b> 
                    <br> Štátna príslušnosť hráča. 
                    <br><br>
                    <b> Mužstvo </b> 
                    <br> Mužstvo, s ktorým má hráč momentálne podpísanú zmluvu. 
                    <br><br>
                    <b> Kvalita </b> 
                    <br> 
                    Veľmi dôležitá vlastnosť hráča, ktorá síce neovplyvňuje priamo jeho výkon na zápasoch, ale má veľký vplyv na rýchlosť tréningu. Navyše, keď atribút hráča dosiahne úroveň 250% kvality, tréning toho atribútu sa výrazne
                    spomalí. Kvalita je určená výpočtom už pri "narodení" hráča a neskôr sa nemení. Hráči s kvalitou menšou ako 80 pri vyhodení z mužstva budú nenávratne vymazaný z hry a hráči s kvalitou väčšou ako 80 idú na trh ako voľní hráči. 
                    <br><br>
                    <b> Potenciál </b> 
                    <br> 
                    Každý hráč má vekový horizont. Keď ho dosiahne, začnú sa zhoršovať jeho schopnosti. Hodnota potenciálu sa postupne znižuje, ako hráč starne a potenciál 0 % znamená, že hráč dosiahol svoj vekový horizont. Hráč
                    s potenciálom 0% po zostarnutí si môže uchovať 0% potenciál ešte ďalší HA rok, toto sa môže stať pre hráča posledný krát pri zostarnutí z 33 na 34. Jeden hráč môže takto zostarnúť aj viackrát za svoj “život”. Praktický rozdiel
                    medzi hráčmi s potenciálom 20 % a 50 % je taký, že prvý dosiahne skôr svoj vekový horizont a začnú sa mu skôr zhoršovať schopnosti.
                    <br>
                    Čo sa týka záporného potenciálu, tak rýchlosť klesania atribútov nájdete v sekcii <a href="index.php?navod=klesanie_atributov_%"> Tréning - Klesanie atribútov</a> .
                    <br><br>
                    <b> Index šikovnosti </b> 
                    <br> 
                    Index šikovnosti je súčet hlavných atribútov hráča (bez formy a skúsenosti). 
                    <br><br>
                    <b> Slabé miesto brankára </b>
                    <br> 
                    Strelci, ktorí strieľajú na brankárove slabé miesto, majú lepšiu šancu dať gól. Tento atribút vidí iba aktuálny majiteľ hráča. 
                    V nastavení zápasu je možné zadať, na ktoré miesto pôjde väčšina striel mužstva alebo je možné nechať normálne nastavenie, v tom prípade sa streľba rozdelí rovnomerne. Toto nastavenie sa zobrazuje aj na stránke odohraného zápasu - iba na vlastných zápasoch. 
                    <br><br>
                    <b> Spokojnosť</b> 
                    <br> 
                    Spokojnosť hráča funguje od jeho 23 roka a je ovplyvnená tým, ako často hráva ligové zápasy. Začína na úrovni 0% a hýbať smerom hore (100%) alebo dole (-100%) sa začína vtedy, ak je hráč v týme viac ako 9-týždňov. Každý ďaľší týždeň nasleduje pohyb o 18% ak 
                    hráč hráva pravidelne a má odohrané viac ako <sup>3</sup>/<sub>4</sub> zápasov. Ak hráč hráva sporadicky a má odohrané menej ako <sup>1</sup>/<sub>4</sub> zápasov, tak spokonnosť klesá o -18%. V prípade, že hráč hráva striedavo a má odohrané 
                    medzi <sup>1</sup>/<sub>4</sub> - <sup>3</sup>/<sub>4</sub> zápasov, tak spokojnosť stagnuje a nehýbe sa. 
                    Spokojnosť má význam len u nechránených hráčov – viac informácií je v časti <a href="index.php?navod=nechraneni_hraci">Trh</a> . 
                    <br><br> 
                    Ak je hráč v mužstve spokojný, domácemu mužstvu stačí ponúknuť 75-100% najvyššej ponuky (podľa spokojnosti hráča), ak je nespokojný, musí ponúknuť 100-130% najvyššej ponuky.
