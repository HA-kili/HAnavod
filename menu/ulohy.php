
                <div class="section-name"> Úlohy </div>
                    <br>
                    Pre nováčikov <u>Hockey Areny</u> je spustený systém, ktorý pomocou série jednoduchých úloh naučí manažéra základom manažovania mužstva. Za každú splnenú úlohu získa mužstvo aj malý finančný bonus. Tieto úlohy môžu samozrejme plniť aj "veteráni"
                    a postupne budeme do tohto systému pridávať ľahké (aj ťažké) úlohy pre všetkých manažérov. 
                    <br><br> 
                    Úlohy nájdete v menu <a href="https://www.hockeyarena.net/sk/index.php?p=manager_achievements.inc&action=list">Manažér - Úlohy</a> . 
