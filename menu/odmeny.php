
                <div class="section-name"> Odmeny </div>
                    <br>
                    V Sekcii <a href="https://www.hockeyarena.net/sk/index.php?p=manager_league_bonuses.php">Liga - Odmeny</a> je možné nájsť výšku odmien za umiestnenie mužstva :
                    <br><br> 
                    <ul> 
                        <li> <u> po základnej časti </u> - budú vyplatené v utorok po prepočte
                        <li> <u> za štatistiky </u> - budú vyplatené v stredu po prepočte
                        <li> <u> v Playoff </u> - budú vyplatené vo štvrtok po prepočte
                        <li> <u> v Národnom pohári </u> - budú vyplatené v stredu po prepočte 
                        <li> <u> vo Svetovej lige </u> - budú vyplatené v stredu po prepočte
                        <li> <u> v Pohári národných šampiónov </u> - budú vyplatené v stredu po prepočte
                    </ul>
                    <br>
                    <ul>
                        <li> <u> Odmena za postup do vyššej ligy </u> - je vyplatená v sobotu po "veľkom prepočte"
                    </ul>
