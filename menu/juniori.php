                    Ak máte Juniorku na 50 %, dostávate 5 juniorov za týždeň. Ak vo štvrtok si vrátite Juniorku na 100 % (už ste mali Juniorku na 100 %, ale ste si znížili), do konca týždňa budete dostávať juniorov v takom počte, ako keby ste mali
                    juniorku stále na 50 %. Až v nedeľu sa to zmení a teda od najbližšej nedele budete dostávať v týždni 10 juniorov, čo prináleží vybudovaniu Juniorky na 100%;) 
                    <br><br> 
                    Hodnoty kvality a potenciálu v juniorskej prípravke sú zvyčajne iba automatické nepresné odhady z oddelenia prieskumu a musia byť manuálne preskúmané na získanie presnejších hodnôt. Stačí kliknúť na interval hodnôt v zátvorke napravo od odhadovanej hodnoty, zadať svoj odhad a toto zmenší zistený
                    interval. Toto sa dá opakovať, až kým sa nedosiahne požadovaná presnosť intervalu. Presná hodnota sa nachádza v tomto intervale. 
                    <br><br> 
                    Máš k dispozícii iba obmedzený počet juniorských prieskumov, ak potrebuješ viac, musíš zvýšiť
                    úroveň svojho oddelenia prieskumu. Tieto prieskumy netreba minúť všetky hneď, pretože sa v priebehu sezóny môžu nazbierať, takže o nadbytočné neprídeš. 
                    <br><br> 
