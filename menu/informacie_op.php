                    <h1> Motivácia na dnešný zápas </h1> 
                    <ul>
                        <li> odhad motivácie mužstva na dnešný zápas. Motivácia je ovplyvnená týmito faktormi : motivačná odmena, roztlieskávačky, domáce prostredie, nálada v mužstve, dôležitosť zápasu. </li>
                    </ul>
                    <br>
                    <h1> Cena vstupenky na dnešný zápas </h1> 
                    <ul>
                        <li> odhad ideálnej ceny vstupenky na dnešný zápas. Zobrazuje sa, iba ak sa dnešný zápas hrá doma. </li>
                    </ul>
                    <br>
                    <h1> Sila mužstva </h1> 
                    <ul>
                        <li> odhad sily svojho alebo súperovho mužstva sa počíta ako priemer hlavných atribútov zo zostavy mužstva
                    na poslednom ligovom zápase. B = brána, O = obrana, Ú = útok. </li>
                    </ul>
                    <br>
                    <h1> Tréning </h1> 
                    <ul>
                        <li> priemerný denný tréningový pokrok, počítajú sa iba hráči, ktorí majú individuálny pokrok väčší ako 0 %. </li>
                    </ul>
                    <br>
                    <h1> Denný zisk </h1> 
                    <ul>
                        <li> je rozdiel denných príjmov (príjem z darčekov, ligový sponzori, vstupné) a denných výdavkov. </li>
                    </ul>
