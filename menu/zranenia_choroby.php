
                <div class="section-name"> Zranenie a choroby </div>
                    <br>
                    Hráči sa môžu zraniť na tréningu, na zápase alebo môžu ochorieť. Pravdepodobnosť zranenia a choroby sa zvyšuje u hráčov, 
                    ktorých energia je <u> nižšia ako 60% </u>. Zranení a chorí hráči netrénujú ani sa neregenerujú a nemôžu hrať zápasy. 
                    <br><br>
                    V prípade, že sa hráč zraní v zápase, tak nenatrénuje nič, ale získa skúsenosti. Ak sa zraní počas tréningu (na lavičke), tak 
                    tréning mu zostáva, ale skúsenosti nemá žiadne, kedže zápas nehral.                     
                    <br><br> 
                    Zranení a chorí hráči sa zobrazujú s ikonkou malej lekárničky <img class="icons" src="./icons/lekarnicka.gif" > . 
                    <br><br> 
                    Zranenému hráčovi sa zníži forma o 2 body za každý deň  zranenia. Podľa závažnosti zranenia to môže byť aj viac.  
                    <br><br> 
                    Zraneným hráčom sa dĺžka zranenia mení (zmenšuje) každý deň po prepočte, čiže po 22:00 hod. 
                    <br><br> 
                    Celková dĺžka zranenia hráča za sezónu sa zobrazuje v menu <a class="arena" href="https://www.hockeyarena.net/sk/index.php?p=manager_team_statistics.php#x">Hráči - Štatistiky</a> 
                    (iba pre <a class="arena" href="https://www.hockeyarena.net/sk/index.php?index.php&p=public_shop.php">Sponzorský balík</a>). <a class="novinka" href="https://www.hockeyarena.net/sk/index.php?p=public_ha_news.php&id=13519"><img class="icons" src="./icons/novinka.png"></a>
                    <br><br> 
                    Zranenie hráča je možné zmenšiť pomocou dresu "Zotavenie" (mesiac Jún) – sekcia <a class="navod" href="index.php?navod=specialne_dresy">Dresy</a>.
                    <br><br>
                    Maximálna doba zranenia hráča je 12 dní. 
