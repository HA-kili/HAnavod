
                <div class="section-name"> Hviezda týždňa </div>
                    <br>
                    Umožní hráčom získavať hviezdny štatút a dúfame že trošku spestrí súťaživosť v rámci ligy. 
                    Za každý týždeň sa v lige vyhodnotia tri hviezdy týždňa - <u> brankár, obranca a útočník </u> - na 
                    základe výkonov v ligových zápasoch za uplynulý týždeň. 
                    <br><br>
                    Vyhodnotenie prebehne vždy v sobotu na začiatku prepočtu a víťazní hráči získajú zvýšený hviezdny štatút. 
                    Informácia o tom, že sa hráč stal hviezdou týždňa sa objaví v novinkách mužstva, manažéri 
                    so <a href="https://www.hockeyarena.net/sk/index.php?index.php&p=public_shop.php"> sponzorským balíkom </a>  navyše 
                    uvidia v okne <a href="https://www.hockeyarena.net/sk/index.php?p=public_standings.inc"> Liga - Tabuľky </a> aktuálne 
                    hviezdy týždňa. 
                    <br><br>
                    Pri brankároch sa berie do úvahy ten brankár, na ktorého v rámci jedného zápasu a jedného mužstva išlo viac striel. 
                    Ak išiel na oboch brankárov rovnaký počet striel, tak sa zoberú do úvahy obaja. Brankár sa bude vyhodnocovať aj 
                    zobrazovať podľa výkonu zo súčtu jeho striel. 
                    <br><br>
                    Pri vyhodnocovaní hviezd týždňa sa berie do úvahy počet zápasov, ich výsledok (výhry/prehry) a v neposlednom rade 
                    štatistiky hráčov v danom týždni. 
