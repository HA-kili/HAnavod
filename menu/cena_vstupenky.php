
                <div class="section-name"> Cena vstupenky </div>
                    <br>
                    Cena vstupenky sa použije na domácich zápasoch. Peniaze zo vstupného sú hlavným príjmom každého mužstva, preto treba pred každým domácim zápasom uvážlivo nastaviť cenu vstupenky. Ako pomôcka funguje <a class="arena" href="https://www.hockeyarena.net/sk/index.php?p=manager_scouting_form.php">Oddelenie prieskumu</a> , kde
                    je vždy odhadnutá ideálna cena vstupenky z pohľadu príjmu. Hra sa snaží simulovať divácku zaujímavosť každej ligy podľa rozdielov medzi mužstvami. Cena vstupného na zápas sa nastavuje v menu <a class="arena" href="https://www.hockeyarena.net/sk/index.php?p=manager_tactics_form.php">Mužstvo - Nastavenie zápasu</a>. 
                    <br><br>
                    Tento systém počíta s tým, že v lige, kde hrá väčšie množstvo približne rovnako kvalitných mužstiev, sa hrajú napínavejšie zápasy, zaujímavé pre divákov, ktorí sú preto ochotní platiť vyššiu cenu vstupenky. 
                    V zápasoch o umiestnenie je výška vstupného v treťom zápase počítaná približne ako v priateľskom zápase. 
                    <br><br>
                    <b> POZOR!!!   Príliš vysoká cena vstupenky môže spôsobiť malú návštevnosť na zápase. </b>
                    <br><br>
                    V každom detailnom zobrazení odohraného zápasu je možné skontrolovať, koľko divákov sa prišlo pozrieť na zápas a aký bol celkový príjem zo vstupného.
                    <br><br><br><br>
                    <u> Prehľad prerozdelenia príjmov zo vstupného : </u>
                    <br>
                    <i><u><h1>Ligové zápasy</u></i></h1>
                    Celý príjem zo vstupného dostáva domáce mužstvo.
                    <br>
                    <i><u><h1>Priateľské zápasy</u></i></h1>
                    Zisk zo zápasu sa delí na polovicu medzi súperov a na motiváciu hráčov nemá vplyv domáce prostredie. Preto je lepšie hrávať priateľské zápasy u manažéra z vyššej ligy a s väčším štadiónom.
                    <br><br><br><br>
                    <i><u><h1>Zápasy vo svetovej lige a v národných pohároch</u></i></h1>
                    Zisk zo zápasu sa delí na polovicu medzi súperov a na motiváciu hráčov nemá vplyv domáce prostredie.
