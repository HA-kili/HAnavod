
                <div class="section-name"> Kľúčový hráč </div>
                    <br> 
                    Existuje možnosť ako si udržať v mužstve hráčov s nižšou lojalitou. Raz za 40 týždňov môže manažér zvoliť <b> tzv. kľúčového hráča </b> mužstva. Kľúčový hráč nepôjde automaticky na trh s nechránenými hráčmi, ale nebude možné ho ani
                    predať ani vyhodiť. Túto možnosť nájdete tak, že kliknete na malú ikonku <img class="icons" src="./icons/plusko.gif" > pri lojalite na karte hráča. Po kliknutí na túto ikonku sa dozviete aj podrobnejšie informácie o tejto novej fukcii. 
                    <br><br>
                    <b><h1> Kľúčového hráča je možné mať IBA v hlavnom týme. </h1></b>
