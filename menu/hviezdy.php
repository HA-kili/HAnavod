
                <div class="section-name"> Hviezdy </div>
                    <br><br> 
                    Počet hviezd <img class="icons" src="./icons/hviezda.gif" > vyjadruje hviezdny štatút hráča. V každom ligovom zápase sa vyhlásia tri najväčšie hviezdy (možnosť vidieť vo výpise zápasu pod zostavou), títo hráči získavajú väčšiu popularitu medzi fanúšikmi a postupne sa z nich stanú hviezdy.
                    <br><br> 
                    Na karte hráča, ktorý zatiaľ nezískal žiadnu hviezdu, je možné sledovať jeho pokrok k hviezdnemu štatútu ako percentuálnu hodnotu z oddelenia prieskumu. 
                    <br><br>
                    Ďalší faktor, ktorý zvyšuje hviezdny štatút hráčov, sú štatistické kategórie
                    vyhodnotené vždy na konci základnej časti sezóny.
                    <br><br> 
                    <b> Momentálne sú to tieto : </b> 
                    <br>
                    <i><u> Kanadské bodovanie produktivity </u></i> - poradie hráčov podľa počtu strelených gólov a nahrávok na góly. Hráč musí odohrať aspom 20 zápasov (600 minút) aby sa mohol umiestniť v koncosezónnych TOP štatistikách. 
                    Koncosezónne štatistiky hráča sa budú ukladať pre všetkých hráčov v hre, ale viditeľné budú iba pre manažérov so <a href="https://www.hockeyarena.net/sk/index.php?index.php&p=public_shop.php">sponzorským balíkom</a>. 
                    <br><br>
                    <i><u> Najlepší strelec </u></i> - poradie hráčov podľa počtu strelených gólov.
                    <br><br>
                    <i><u> Najlepší obranca  </u></i> - poradie obrancov podľa počtu strelených gólov a nahrávok na góly. Do tejto kategórie sa počíta každý hráč, ktorý všetky ligové zápasy odohral na pozícii obrancu. V štatistike bodovania obrancov sa bude vyhodnocovať 20 najlepších hráčov. 
                    <br><br>
                    <i><u> Najlepší brankár</u></i> - poradie brankárov podľa percentuálnej úspešnosti zásahov. Do tejto kategórie sa počítajú iba brankári, ktorí odohrali aspoň 20 ligových zápasov (900 minút) v pozícii brankára - 5 TOP hráčov v lige.
                    <br><br> 
                    <i><u> Hattricky </i></u> - nevyhodnocuje sa pri hviezdach. 
                    <br><br> 
                    <u><i> Shutout-y </i></u> - nevyhodnocuje sa pri hviezdach. 
                    <br><br> 
                    <u><i> Úspešnosť pri vhadzovaniach </i></u> – 10 TOP hráčov v lige. 
                    <br><br> 
                    <u><i> Trestné minúty </i></u> - 10 TOP hráčov s najmenej trestnými minútami v pomere k času na ľade. 
                    <br><br> 
                    <u><i> Odohrané minúty na zápas pre hráčov v poli </i></u> - 10 TOP hráčov v lige. 
                    <br><br><br>
                    <b> Umiestnenie v týchto hodnoteniach je prvá podmienka na to, aby sa dal hráčovi darovať dres. </b> 
                    <br><br><br>
                    A načo sú mužstve hviezdni hráči? Okrem toho, že hviezdy chcú vyššie platy, má to aj dobrú stránku - na zápasy chodí viac divákov a predávajú sa darčekové predmety. Hviezdni hráči sa dajú prehľadne sledovať v menu <a href="https://www.hockeyarena.net/sk/index.php?p=manager_stars_form.php">Hráči - Hviezdy</a> 
                    a v tomto okne sa dajú aj kupovať darčekové a zberateľské predmety. Základná nákupná cena týchto predmetov je 5000 za kus a pri jednorazovom nákupe väčších množstiev sa znižuje. Predajná cena je 15000. </p>
                    <br> 
                    Predaj sa vyhodnocuje raz týždenne v stredu a je ovplyvnený počtom a úrovňou hviezdnych hráčov mužstva a úrovňou oddelenia marketingu. Maximálny počet darčekových predmetov je 650 ks. 
                    <br><br> 
                    Hviezdny hráči zvyšujú návštevnosť na zápasoch. Pri predaji hráča do inej ligy, alebo vyhodení, sa hráčovi hviezdny status o niečo zníži. Ak je zoznam hviezdnych hráčov prázdny, nebudú sa predávať žiadne darčekové predmety !!
