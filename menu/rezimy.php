
                <div class="section-name"> Režimy </div>
                    <br>
                    Tréningový režim je nastavenie činností, ktoré bude hráč trénovať počas tréningu. Je možné vytvoriť až 30 režimov podľa toho, ktoré vlastnosti sa v režime budú trénovať a ako dlho. 
                    <br><br>
                    <u> Napríklad tréningový režim pre centra môže vyzerať takto : </u>
                    <br> 
                    Útok: 40%, Streľba: 30%, Sila: 30%, Oddych: 0% 
                    <br> 
                    Nazvime ho "Utok-100" 
                    <br> 
                    Toto nastavenie znamená, že hráč bude 40% možného času na tréning venovať zlepšovaniu atribútu útok, 30% času venuje streľbe a 30%
                    času sile. 
                    <br><br>
                    Pri samotnom vykonaní tréningového režimu ovplyvňuje celkový čas tréningu jednotlivých atribútov aj nastavenie intenzity tréningu. Tú je možné zmeniť v menu <a href="https://www.hockeyarena.net/sk/index.php?p=manager_training_schedules_form.php">Hráči - Režimy</a> a platí iba pre hráčov, ktorí nehrajú zápas. Hráči,
                    ktorí hrajú zápas, si intenzitu tréningu po zápase zvolia automaticky, podľa aktuálnej únavy.
                    <br><br>
                    <u> Ak je nastavená intenzita tréningu 80 % </u>
                    <br> 
                    hráč, ktorý je zaradený do režimu "Utok-100" rozdelí svoj tréningový čas nasledovne : 
                    <br> 
                    Útok: 32%, 
                    <br> 
                    Streľba: 24%, 
                    <br> 
                    Sila: 24%, 
                    <br> 
                    Oddych: 20% 
                    <br><br>
                    Hráč trénuje jednotlivé atribúty v časoch podľa nastavenia režimu a intenzity, celý zostávajúci čas sa venuje aktívnemu oddychu.
                    <br><br>
                    Oddych hráča je možné nastaviť aj priamo v tréningovom režime tak, že pre tréning atribútov nepoužijeme 100% času, nepriradený čas sa automaticky použije ako oddych. Ak teda zmeníme režim "Utok-100" :
                    <br> 
                    Útok: 20%, 
                    <br> 
                    Streľba: 15%, 
                    <br> 
                    Sila: 15%, 
                    <br> 
                    Oddych: 50% 
                    <br> 
                    a nazveme ho "Utok-50",
                    <br><br>
                    Samotný tréning hráča sa vykoná takto (pri nastavenej intenzite tréningu 80 %) : 
                    <br> 
                    Útok: 16%, 
                    <br> 
                    Streľba: 12%, 
                    <br> 
                    Sila: 12%, 
                    <br> 
                    Oddych: 60% 
                    <br><br>
                    <b> Pre hráčov, ktorí hrajú zápas, nemá zmysel nastavovať oddych priamo v tréningovom režime – títo hráči oddychujú automaticky po zápase podľa aktuálnej únavy! </b>
                    <br><br>
                    <b> Hráči, ktorí majú nastavený systémový tréningový režim 'Oddych' netrénujú (iba keď hrajú zápas) a oddychujú menej! 
                    <br><br>
                    Hráč s energiou nižšou ako 20% nevládze trénovať a automaticky si zvolí systémový režim 'Oddych'! 
