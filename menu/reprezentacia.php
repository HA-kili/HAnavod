                    <br>
                    <b> Majstrovstvá sveta </b>
                    <br> 
                    <a href="https://www.hockeyarena.net/sk/index.php?p=manager_national_wc.php&type=senior">Majstrovstvá sveta seniorov</a> sa konajú každú nepárnu sezónu a voľby manažérov do seniorských reprezentačných mužstiev prebiehajú počas prvého týždňa párnej sezóny. 
                    <br><br> 
                    <a href="https://www.hockeyarena.net/sk/index.php?p=manager_national_wc.php&type=u20">Majstrovstvá sveta hráčov do 20 rokov</a>  (U20) sa konajú každú párnu sezónu a voľby prebiehajú počas prvého týždňa nepárnej sezóny. 
                    <br><br> 
                    Reprezentačné zápasy sa hrajú v sobotu.
                    <br><br>
                    <b> Voľby </b>
                    <br> Kandidovať vo voľbách môže každý manažér, voliť je možné iba vo svojej skutočnej krajine (podľa IP adresy). Každý manažér má iba jeden hlas a je možné ho zmeniť. Voliť sa môže aj v krajine, kde má manažér svoj B-tým. 
                    Výsledky volieb sú v nedeľu 00:01. V prípade, že výsledok volieb je nerozhodný, rozhodne náhodný faktor (NF) v pondelok po 19:00 hod. 
                    <br><br>
                    <b> Hráči </b>
                    <br> 
                    Hráči základnej zostavy v reprezentácii chýbajú svojmu materskému klubu v sobotných zápasoch. Plat týchto hráčov platí reprezentácia a nie materský klub. Hráči majú vyšší tréning, väčšie skúsenosti a hráčov si viac všímajú iní
                    manažéri, čo zväčšuje jeho trhovú cenu. Hráči národného mužstva budú vždy zobrazení s národným dresom. Maximálny počet hráčov v reprezentácii je 35.
                    <br><br>
                    <b> Prieskum </b>
                    <br> 
                    Reprezentačný manažér má k dispozícii špeciálny prieskum na vyhľadávanie hráčov vhodných do mužstva. Tento prieskum sa vykonáva v konkrétnej lige, zvolením možnosti "Preskúmaj" 
                    v okne <a href="https://www.hockeyarena.net/sk/index.php?p=public_league_info.php"> Liga - Tabuľka - Info</a> . Je možné ho použiť 1 raz za deň. Iná možnosť ako prekúmavať hráčov je 
                    klasickým spoôsobom a to v karte hráča po kliknutí na kolonku "Prieskum" a to za peniaze alebo kliknutím na karte hráča na "kvalitu / potenciál", kde systém za preskúmanie hráča 
                    zoberie 3 prieskumy z juniorky. 
                    <br><br>
                    <b> Nálada </b>
                    <br> Nálada v reprezentácii klesá a stúpa podľa nastavenia dôležitosti zápasov (náladu neovplyvňujú priateľské zápasy). Počiatočná nálada na začiatku každej sezóny je chladná.
                    <br><br>
                    <b> Špeciálne pravidlá </b>
                    <br> 
                    V prípade reštartovania mužstva, v ktorom hrajú reprezentanti, títo nie sú vymazaní, ale opustia mužstvo po zániku (nie po odchode manažéra) a automaticky sa dostanú na trh.
                    <br><br> 
                    Z reprezentácie nie je možné vyhodiť hráča, ktorý je v mužstve menej ako 2 týždne. Počet týždňov strávených v reprezentácii sa zobrazuje pre každého hráča v okne <a href="http://www.hockeyarena.net/sk/index.php?p=national_players.php"> Repre, Hráči  </a> v stĺpci Týž.
                    <br><br> 
                    Pridávať a vyhadzovať hráčov nie je možné počas sezóny, v ktorej prebiehajú majstrovstvá sveta. Výnimka je posledný a prvý týždeň sezóny vtedy je možné meniť hráčov bez ohľadu na majstrovstvá sveta.
