                    <h1>Pridelenie mužstva</h1>
                    <br>
                    <b> Prideľovanie mužstva </b> prebieha automaticky počas večerného prepočtu. Po pridelení mužstva príde manažérovi automaticky vygenerované e-mailové upozornenie. Každé pridelené mužstvo má vygenerovaných úplne nových hráčov, financie,
                    štadión a zázemie nastavené na počiatočné hodnoty. 
                    <br><br>
                    Po pridelení mužstva je <b> nevyhnutné v priebehu 6 dní aktivovať </b> mužstvo a to prihlásením sa do hry. Následne je k dispozícii nastavenie názvu mužstva a neskôr v nastaveniach aj <a class="arena" href="https://www.hockeyarena.net/sk/index.php?p=manager_settings_form.php">názov štadióna</a>. 
                    Novoregistrovaní manažéri budú dostávať pevne daných 28 hráčov bez možnosti výberu ale lepšie konkurencieschopných vo svojej lige.
                    <br><br><br><br>
                    <h1>Zánik mužstva</h1>
                    <br>
                    Manažér bude z mužstva vyhodený v týchto prípadoch : 
                        <ul>
                            <li>ak je neaktívny 42 dní (platí pre A mužstvo)</li>
                            <li>ak je nekatívny 21 dní (platí pre B mužstvo)</li>
                            <li>ak sa neprihlási do mužstva počas 6 dní od pridelenia mužstva (počíta sa od prepočtu)</li>
                            <li>ak má mužstvo menej ako 20 hráčov</li>
                        </ul>
                    <br>
                    Ďalšie informácie : 
                    <br>
                        <ul>
                            <li>ak bude manažér z mužstva vyhodený, tak idú všetci hráči z reprezentácie na voľný trh</li>
                            <li>samotné mužstvo zanikne, až keď sa zaregistruje nový manažér a bude mu toto mužstvo pridelené</li>
                        </ul>
