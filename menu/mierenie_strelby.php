
                <div class="section-name"> Mierenie streľby </div>
                    <br>
                    Strelci, ktorí strieľajú na brankárove slabé miesto, majú lepšiu šancu dať gól. V menu <a class="arena" href="https://www.hockeyarena.net/sk/index.php?p=manager_tactics_form.php"> Mužstvo - Nastavenie zápasu</a> je možné zadať, 
                    na ktoré miesto pôjde väčšina striel mužstva alebo je možné nechať normálne nastavenie. V tom prípade sa streľba rozdelí rovnomerne. Toto nastavenie sa zobrazuje aj na stránke odohraného zápasu.
