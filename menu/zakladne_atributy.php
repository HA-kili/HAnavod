
                <div class="section-name"> Základné atribúty </div>
                    <br>
                    <u> Každý hráč má 11 základných atribútov </u>, ktoré ovplyvňujú jeho výkon na zápase, a ďalšie, ktoré majú vplyv na iné oblasti hry.
                    <br><br>
                    <b> Brána </b>
                    <br> Schopnosť hráča hrať na pozícii brankára, spolu s rýchlosťou ovplyvňuje úspešnosť pri blokovaní striel súpera na bránu.
                    <br><br>
                    <b> Obrana  </b>
                    <br> Obrancovia využívajú tento atribút ako hlavný pri bránení súperovým útokom. Samozrejme, obranu používajú pri obrannej činnosti aj útočníci, ale menej ako obrancovia, pretože ich hlavnou úlohou je útočiť.
                    <br><br>
                    <b> Útok  </b>
                    <br> Schopnosť a šikovnosť hráčov v poli pri útočení na súperovu bránu využívaná hlavne útočníkmi (rovnakým ako útočníci využívajú pri bránení atribút Obrana, využívajú aj obrancovia pri útočení atribút Útok).
                    <br><br>
                    <b> Streľba  </b>
                    <br> 
                    Streľbu používajú všetci hráči v poli pri strieľaní gólov. Nechytateľné strely - tento faktor pribudol do zápasov s cieľom zvýšiť dôležitosť dobrých strelcov v zostave - fungovať to bude tak, že dobrý strelec má šancu streliť nechytateľnú strelu, ktorou prekoná skoro každého brankára. 
                    <br><br>
                    <b> Nahrávka/Kontrola puku  </b>
                    <br> 
                    Pre hráčov v poli – dobrá alebo zlá nahrávka zvyšuje (resp. znižuje) šancu, že strela hráča skončí v sieti. Dôležitá schopnosť pre centrov – pretože ich ulohou je rozohrávať akcie a rozdávať gólové
                    prihrávky ostatným hráčom. 
                    <br><br> 
                    Pre gólmanov – nahrávka sa pre gólmanov používa ako 'Kontrola puku/Nahravka' a ovplyvňuje šikovnosť pri spracovaní a rozohrávke puku po zachytení strely. Nízke hodnoty tohto atribútu spôsobia častejšie
                    možnosti dorážok pre súperových hráčov. Pri dorážke je vyššia šanca na skórovanie ako pri normálnej strele.
                    <br><br>
                    <b> Sila  </b>
                    <br> 
                    Pri vhadzovaní využíva center svoju silu (spolu s atribútom Útok) a všetci hráči v poli využívajú silu pri bránení. V kombinácii s vhodným nastavením agresivity formácie môže sila zvýšiť celkovú prevahu mužstva.
                    <br><br>
                    <b> Rýchlosť  </b>
                    <br> 
                    Využívaná všetkými hráčmi, ako hlavný atribút brankármi a pomocný pre ostatných hráčov.
                    <br><br>
                    <b> Sebaovládanie  </b>
                    <br> 
                    Pre hráča v poli – pri agresívnej hre spôsobuje nízke sebaovládanie častejšie vylučovanie hráča. Počet vylúčení závisí prevažne od následnej kombninácii : nastavenia agresivity formácií a pomer vášho a súperovho sebaovládania.
                    <br> 
                    Pre brankára – nízke sebaovládanie brankára môže na zápase spôsobiť po sérii inkasovaných gólov v rýchlom slede pokles jeho sebadôvery a koncentrácie. To bude mať za následok zhoršenie jeho výkonu až do konca zápasu. V reporte
                    zo zápasu je táto udalosť označená hlásením <u> 'Brankár stratil sebadôveru a koncentráciu' </u>.
                    <br>
                    <b> Sebaovládanie hráčov mladších ako 41 rokov nebude odteraz vôbec klesať. </b>
                    <br><br><br> 
                    Tieto atribúty sa zlepšujú počas tréningu aj počas zápasu. Na zápase sa zlepšujú podľa toho, na akej pozícii hráč hrá. Nastavenie tréningu a spôsob zlepšovania týchto atribútov sú podrobne popísané v časti návodu <a href="./menu/trening.html">Tréning</a> .
                    <br><br><br> <br>
                    <u> Ďalšie atribúty </u>, ktoré priamo ovplyvňujú výkon hráča na zápase, ale menia sa podľa iných pravidiel ako predchádzajúca skupina :
                    <br><br><br>
                    <b> Energia </b> 
                    <br> 
                    Na zápase sa hráč unavuje podľa času stráveného na ľade, preto sa mu znižuje energia. Brankár sa na zápase unavuje podľa počtu striel na bránu. Strata energie každého hráča ovplyvňuje jeho výkon takto - ak hráč
                    stratí 30 % energie, atribúty brána, obrana, útok, streľba, sila a rýchlosť sú počas zápasu oslabené o 30 %. Toto platí aj vtedy, keď hráč nastupuje na zápas so zníženou energiou.
                    <br> 
                    Energia hráčov klesá a stúpa aj počas tréningu podľa nastavenia jednotlivých fáz. Hráči sa každý deň regenerujú navyše aj počas nočného oddychu, bez ohľadu na to, ako je nastavený tréning. Veľmi unaveným hráčom (menej ako 60%
                    energie) sa zvyšuje riziko zranenia.
                    <br> 
                    Ak sú TP a RP vyrovnané, tak pri 100% oddychu denne stúpne energia hráča o 33 bodov. 
                    <br>
                    V detailom pohľade na zápas je možné sledovať energiu hráčov tesne pred zápasom a tesne po zápase v stĺpci Ene.
                    <br><br><br>
                    <b> Forma, skúsenosť </b>
                    <br> 
                    Vysoká forma a skúsenosť spôsobujú, že hráč bude efektívne a správne využívať všetky svoje schopnosti. Naopak v prípade nízkej formy a skúsenosti je vyššia šanca, že hráč bude "kaziť, čo sa dá".
                    <br><br> 
                    Formou sa vyjadruje momentálna hráčova fyzická a psychická pohoda. Preto nie je priamo ovplyvnená výkonom hráča na zápase ani tréningom. Faktory, ktoré ovplyvňujú formu, sú všetky udalosti, ktoré sa týkajú hráča (napr. jeho
                    osobný život), ale nie sú podchytené v iných oblastiach HA. Forma hráčov kolíše hore a dole, najlepší spôsob, ako to ovplyvniť, je hráčov s nízkou formou nahradiť v zostave hráčmi s vysokou formou. V každom momente má forma smer, ktorým
                    sa pohybuje, alebo zostáva rovnaká. Tento smer sa môže zmeniť počas každého prepočtu a šanca zmeny smerom hore sa dá mierne ovplyvniť jednou z týchto možností :
                    <ul>
                        <li> ľahký alebo stredne ťažký tréning (týka sa iba hráčov, ktorí nehrajú zápas) </li>
                        <li> dobrý výkon hráča na zápase </li>
                        <li> vyradenie hráča zo zostavy červenou rukou <img class="icons" src="./icons/cervena_ruka.png" > (<a href="https://www.hockeyarena.net/sk/index.php?index.php&p=public_shop.php">možnosť iba pre Sponzorský Balík</a> ), hráč musí mať odohraté min polovicu 
                            zápasov aktuálneho kola, jeden zápas zastaví klesanie, popr. začne stúpať, dva zápasy sú potrebné aby hráčovi klesajúca forma začala stúpať. Toto platí v ligových zápasoch, zápasoch Národného pohára (NP), Svetovej ligy (SL) a Pohára národných šampiónov (PNŠ). 
                            Upozorňujeme, že zmenu červenou rukou treba uskutočniť až po prepočte (cca po 22:20), v opačnom prípade sa zmeny červenou rukou anulujú.  </li>
                    </ul>
                    <br><br>
                    Zraneným hráčom klesá forma pomaly počas celej doby trvania zranenia.
                    <br><br><br> 
                    Skúsenosť získavajú hráči iba na zápasoch. Najviac skúsenosti je možné získať počas dôležitých zápasov, napríklad v playoff alebo v reprezentácii. 
                    <br>
                    Skúsenosť hráčov na zápase bude ovplyvnená časom stráveným na ľade. Náhodný faktor pre pribúdanie skúsenosti bol mierne znížený. Skúsenosť hráčov na zápase sa bude zvyšovať rýchlejšie vo vyšších ligách a pomalšie v nižších ligách. 
