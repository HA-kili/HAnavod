                        <li> Výška kreditu na sezónu : <b> 1000 </b></li>
                        <li> Uzávierka kancelárie je o <b> 22:00 CET </b></li>
                        <li> Nie je dovolené tipovať na zápas vlastného tímu.
                        <li> Každý zápas možno natipovať len raz denne. </li>
