                    <b><h4> Národný pohár - NP </h4></b>
                    <a href="https://www.hockeyarena.net/sk/index.php?p=manager_tourney_details_form.php&inst_id=190248">Národný pohár</a> hrajú všetky tímy, ktoré sú v 1. až 5. lige. Výnimku tvoria tímy, ktoré hrajú Svetovú Ligu a Pohár národných šampiónov. 
                    Národný pohár sa hrá len v stredu formou vyraďovacích zápasov. Hrá sa na jeden zápas. Spravidla začína prvú stredu sezóny. Ak sú krajiny, kde je menej líg, napríklad len 3. ligy, Národný pohár začína neskoršiu stredu podľa toho, aký počet tímov 
                    sa v krajine nachádza. V prípade, že krajina má dostatočný počet tímov v 1. až 5. lige, prvoligové tímy a niektoré druholigové tímy postupujú priamo do druhého kola. Počas prvého kola Národného pohára budú mať tieto tímy voľno v kalendári 
                    a môžu si naplánovať priateľský zápas. Posledný zápas, finále, sa odohrá 9. stredu sezóny. Posledná streda sezóny je voľná.
