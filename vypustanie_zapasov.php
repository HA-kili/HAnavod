
                <div class="section-name"> Vypúšťanie zápasov </div>
                    <br>
                    Zmenili sme ako systém identifikuje "vypustený" playoff zápas. Systém spočíta priemerné IŠ hráčov na zápase s časom na ľade aspoň 2 minúty a ak je 
                    tento priemer menší ako 80% priemerného IŠ 17tich najlepších hráčov mužstva tak pre tieto účely sa zápas považuje za vypustený a nasleduje:
                    <br><br>
                    <a href="https://www.hockeyarena.net/sk/index.php?p=public_ha_news.php&id=13120"> <u> "Na playoff zápase nehrali naše najlepšie formácie" </u></a>
                    <br> 
                    Mužstvo dostane toto info do noviniek po Playoff zápase, v ktorom nenasadil aspoň 1., alebo 2. najlepšie hodnotenú formáciu. Hodnotenie formácií sa 
                    počíta automaticky a je možné si ho skontrolovať na karte odohraného zápasu v časti formácií, stĺpec "Hod". 
                    <br><br> 
                    V tomto prípade súperove mužstvo dostane do noviniek info: 
                    <br> 
                    <a href="https://www.hockeyarena.net/sk/index.php?p=public_ha_news.php&id=13149"> <u> "Súper nepoužil najlepšiu zostavu v playoff zápase, naši hráči sa vzdali odmien za výhru" </u></a>
                    <br> 
                    A efekt je taký, že súperove mužstvo nevyplatí hráčom bonus za výhru v tomto zápase, aj keď ich motivácia bola ako keby bonusy boli vyplatené.
